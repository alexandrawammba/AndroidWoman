package com.androidwoman.entities;

public class Cycle {

	private int id_cycle;
	private String begDate;
	private String finDate;
	private String begPeriode;
	private String endPeriode;
	public int getId_cycle() {
		return id_cycle;
	}
	public void setId_cycle(int id_cycle) {
		this.id_cycle = id_cycle;
	}
	public String getBegDate() {
		return begDate;
	}
	public void setBegDate(String begDate) {
		this.begDate = begDate;
	}
	public String getFinDate() {
		return finDate;
	}
	public void setFinDate(String finDate) {
		this.finDate = finDate;
	}
	public String getBegPeriode() {
		return begPeriode;
	}
	public void setBegPeriode(String begPeriode) {
		this.begPeriode = begPeriode;
	}
	public String getEndPeriode() {
		return endPeriode;
	}
	public void setEndPeriode(String endPeriode) {
		this.endPeriode = endPeriode;
	}
	
	
}
