package com.androidwoman.entities;

public class AndroidWoman {

	private int id_androidwoman;
	private String name;
	private String surname;
	private String email;
	private int cycleDuration;
	private int menstruationDuration;
	
	public int getId_androidwoman() {
		return id_androidwoman;
	}
	public void setId_androidwoman(int id_androidwoman) {
		this.id_androidwoman = id_androidwoman;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSurname() {
		return surname;
	}
	public void setSurname(String surname) {
		this.surname = surname;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getCycleDuration() {
		return cycleDuration;
	}
	public void setCycleDuration(int cycleDuration) {
		this.cycleDuration = cycleDuration;
	}
	public int getMenstruationDuration() {
		return menstruationDuration;
	}
	public void setMenstruationDuration(int menstruationDuration) {
		this.menstruationDuration = menstruationDuration;
	}
	
	
	
}
